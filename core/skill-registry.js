/**
 * OpenClaw Skill Registry (inspired by ClawHub)
 * Indexes all skills with searchable metadata and usage tracking.
 *
 * Usage:
 *   node skill-registry.js scan          — rebuild index from all skills/
 *   node skill-registry.js search "auth" — fuzzy search skills
 *   node skill-registry.js stats         — usage statistics
 *   node skill-registry.js list          — list all with descriptions
 */

const fs = require('fs');
const path = require('path');

const config = require('./config');
const OPENCLAW = config.paths.root;
const SKILLS_DIR = path.join(OPENCLAW, 'skills');
const REGISTRY_PATH = path.join(OPENCLAW, 'workspace', 'skill-registry.json');
const SESSION_LOG = path.join(OPENCLAW, 'session-log.jsonl');

// ── Core API ────────────────────────────────────────────────────

/**
 * Scan all skills and rebuild the registry index.
 */
function scan() {
  const skills = [];

  try {
    const entries = fs.readdirSync(SKILLS_DIR);

    for (const entry of entries) {
      const skillDir = path.join(SKILLS_DIR, entry);
      const skillMd = path.join(skillDir, 'SKILL.md');

      // Handle both directory-based skills and standalone .js files
      if (fs.statSync(skillDir).isDirectory() && fs.existsSync(skillMd)) {
        const skill = parseSkillMd(skillMd, entry);
        if (skill) skills.push(skill);
      } else if (entry.endsWith('.js')) {
        // Standalone JS skill
        skills.push({
          slug: entry.replace('.js', ''),
          name: entry.replace('.js', '').replace(/-/g, ' '),
          description: `Standalone script: ${entry}`,
          type: 'script',
          autoGenerated: false,
          path: skillDir,
          tags: [],
          projects: []
        });
      }
    }
  } catch (err) {
    console.error(`[skill-registry] Error scanning: ${err.message}`);
  }

  // Enrich with usage stats
  const usage = getUsageStats();
  for (const skill of skills) {
    skill.usageCount = usage[skill.slug] || 0;
  }

  const registry = {
    lastScan: new Date().toISOString(),
    totalSkills: skills.length,
    autoGenerated: skills.filter(s => s.autoGenerated).length,
    skills
  };

  const dir = path.dirname(REGISTRY_PATH);
  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
  fs.writeFileSync(REGISTRY_PATH, JSON.stringify(registry, null, 2), 'utf8');

  return registry;
}

/**
 * Search skills by keyword (name, description, tags).
 */
function search(query) {
  const registry = readRegistry();
  if (!registry) return [];

  const q = query.toLowerCase();
  return registry.skills.filter(s =>
    s.name.toLowerCase().includes(q) ||
    s.slug.toLowerCase().includes(q) ||
    s.description.toLowerCase().includes(q) ||
    s.tags.some(t => t.toLowerCase().includes(q)) ||
    s.projects.some(p => p.toLowerCase().includes(q))
  );
}

/**
 * Get aggregated stats.
 */
function getStats() {
  const registry = readRegistry();
  if (!registry) return null;

  const byType = {};
  for (const s of registry.skills) {
    const type = s.type || 'unknown';
    if (!byType[type]) byType[type] = 0;
    byType[type]++;
  }

  const topUsed = [...registry.skills]
    .sort((a, b) => (b.usageCount || 0) - (a.usageCount || 0))
    .slice(0, 5);

  return {
    total: registry.totalSkills,
    autoGenerated: registry.autoGenerated,
    byType,
    topUsed: topUsed.map(s => ({ name: s.name, uses: s.usageCount })),
    lastScan: registry.lastScan
  };
}

/**
 * Format a one-liner for the morning digest.
 */
function formatForDigest() {
  const registry = readRegistry();
  if (!registry) return '';

  const topSkill = [...registry.skills].sort((a, b) => (b.usageCount || 0) - (a.usageCount || 0))[0];
  const topStr = topSkill && topSkill.usageCount > 0 ? ` | top: ${topSkill.name} (${topSkill.usageCount}x)` : '';

  return `\n*Skills: ${registry.totalSkills} total | ${registry.autoGenerated} auto-generated${topStr}*`;
}

// ── Parsing ─────────────────────────────────────────────────────

function parseSkillMd(filepath, slug) {
  try {
    const content = fs.readFileSync(filepath, 'utf8');

    // Extract YAML frontmatter
    const fmMatch = content.match(/^---\n([\s\S]*?)\n---/);
    const frontmatter = fmMatch ? fmMatch[1] : '';

    // Parse key fields from frontmatter
    const name = extractYaml(frontmatter, 'name') || slug;
    const description = extractYaml(frontmatter, 'description') || '';

    // Check if auto-generated
    const autoGenerated = content.includes('autoGenerated: true');

    // Extract first paragraph after frontmatter as extended description
    const body = content.replace(/^---[\s\S]*?---/, '').trim();
    const firstParagraph = body.split('\n\n').find(p => p && !p.startsWith('#'));

    // Extract tags from metadata or content
    const tags = [];
    const emojiMatch = frontmatter.match(/emoji:\s*"?([^"\n]+)/);
    if (emojiMatch) tags.push(emojiMatch[1].trim());

    // Extract project references
    const projectMatch = frontmatter.match(/projects:\s*\[([^\]]*)\]/);
    const projects = projectMatch
      ? projectMatch[1].split(',').map(p => p.trim().replace(/"/g, ''))
      : [];

    // Determine type
    let type = 'manual';
    if (autoGenerated) type = 'auto-generated';
    else if (fs.existsSync(path.join(path.dirname(filepath), 'index.js'))) type = 'executable';
    else if (fs.existsSync(path.join(path.dirname(filepath), 'scripts'))) type = 'scripted';

    // Count files in skill directory
    const skillDir = path.dirname(filepath);
    const fileCount = fs.readdirSync(skillDir).length;

    return {
      slug,
      name: name.replace(/"/g, ''),
      description: description.replace(/"/g, '').slice(0, 200),
      type,
      autoGenerated,
      path: filepath,
      tags,
      projects,
      fileCount,
      firstParagraph: firstParagraph ? firstParagraph.slice(0, 150) : null
    };
  } catch {
    return {
      slug,
      name: slug,
      description: 'Failed to parse SKILL.md',
      type: 'unknown',
      autoGenerated: false,
      path: filepath,
      tags: [],
      projects: [],
      fileCount: 0
    };
  }
}

function extractYaml(frontmatter, key) {
  const match = frontmatter.match(new RegExp(`^${key}:\\s*(.+)$`, 'm'));
  return match ? match[1].trim() : null;
}

// ── Usage Tracking ──────────────────────────────────────────────

function getUsageStats() {
  const usage = {};
  try {
    const raw = fs.readFileSync(SESSION_LOG, 'utf8');
    const lines = raw.split('\n').filter(Boolean);

    for (const line of lines) {
      try {
        const entry = JSON.parse(line);
        const summary = (entry.summary || '').toLowerCase();

        // Count skill mentions in session log
        const skillDirs = fs.readdirSync(SKILLS_DIR).filter(d =>
          fs.statSync(path.join(SKILLS_DIR, d)).isDirectory()
        );

        for (const skillSlug of skillDirs) {
          if (summary.includes(skillSlug.replace(/-/g, ' ')) || summary.includes(skillSlug)) {
            usage[skillSlug] = (usage[skillSlug] || 0) + 1;
          }
        }
      } catch {}
    }
  } catch {}
  return usage;
}

// ── Internal ────────────────────────────────────────────────────

function readRegistry() {
  try {
    return JSON.parse(fs.readFileSync(REGISTRY_PATH, 'utf8'));
  } catch {
    return scan(); // Auto-build if missing
  }
}

// ── CLI ─────────────────────────────────────────────────────────

if (require.main === module) {
  const cmd = process.argv[2];

  if (cmd === 'scan') {
    const registry = scan();
    console.log(`[skill-registry] Indexed ${registry.totalSkills} skills (${registry.autoGenerated} auto-generated)`);

  } else if (cmd === 'search') {
    const query = process.argv[3];
    if (!query) { console.log('Usage: node skill-registry.js search <query>'); process.exit(1); }
    const results = search(query);
    if (results.length === 0) {
      console.log(`No skills matching "${query}".`);
    } else {
      console.log(`${results.length} skill(s) matching "${query}":`);
      for (const s of results) {
        console.log(`  ${s.slug} (${s.type}) — ${s.description.slice(0, 80)}`);
      }
    }

  } else if (cmd === 'stats') {
    const stats = getStats();
    if (!stats) { console.log('No registry. Run: node skill-registry.js scan'); process.exit(1); }
    console.log(`Skills: ${stats.total} total | ${stats.autoGenerated} auto-generated`);
    console.log(`Types: ${Object.entries(stats.byType).map(([k, v]) => `${k}:${v}`).join(' | ')}`);
    if (stats.topUsed.length > 0) {
      console.log('Top used:');
      for (const s of stats.topUsed) console.log(`  ${s.name}: ${s.uses}x`);
    }

  } else if (cmd === 'list') {
    const registry = readRegistry();
    if (!registry) { console.log('No registry.'); process.exit(1); }
    for (const s of registry.skills.sort((a, b) => a.slug.localeCompare(b.slug))) {
      const auto = s.autoGenerated ? ' [auto]' : '';
      console.log(`  ${s.slug}${auto} — ${s.description.slice(0, 70)}`);
    }

  } else {
    console.log('Skill Registry');
    console.log('  node skill-registry.js scan          — rebuild index');
    console.log('  node skill-registry.js search <q>    — search skills');
    console.log('  node skill-registry.js stats         — usage statistics');
    console.log('  node skill-registry.js list          — list all skills');
  }
}

module.exports = { scan, search, getStats, formatForDigest };
